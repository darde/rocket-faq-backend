from __future__ import annotations

import json
import re

from app.core.llm import chat_completion
from app.observability.logger import get_logger

logger = get_logger(__name__)

JUDGE_SYSTEM_PROMPT = """You are an expert evaluator of RAG (Retrieval-Augmented Generation) systems.
Your job is to evaluate the quality of an answer generated by a mortgage FAQ chatbot.

You will receive:
- The user's question
- The retrieved context (source documents)
- The generated answer

Evaluate on these dimensions (each scored 1-5):

1. **Relevance**: Does the answer address the user's question?
2. **Correctness**: Is the answer factually accurate based on the provided context?
3. **Completeness**: Does the answer cover all important aspects of the question?
4. **Faithfulness**: Does the answer stick to information in the provided context without hallucinating?

Respond ONLY with valid JSON in this exact format:
{
  "relevance": {"score": <1-5>, "reasoning": "<brief explanation>"},
  "correctness": {"score": <1-5>, "reasoning": "<brief explanation>"},
  "completeness": {"score": <1-5>, "reasoning": "<brief explanation>"},
  "faithfulness": {"score": <1-5>, "reasoning": "<brief explanation>"},
  "overall_score": <average of all scores, rounded to 1 decimal>,
  "summary": "<1-2 sentence overall assessment>"
}"""

JUDGE_USER_TEMPLATE = """## User Question
{question}

## Retrieved Context
{context}

## Generated Answer
{answer}

Evaluate the generated answer based on the criteria described."""


def evaluate_response(
    question: str,
    context: str,
    answer: str,
) -> dict:
    """Use LLM-as-judge to evaluate a RAG response."""
    logger.info("judge_evaluation_start", question=question[:80])

    messages = [
        {"role": "system", "content": JUDGE_SYSTEM_PROMPT},
        {
            "role": "user",
            "content": JUDGE_USER_TEMPLATE.format(
                question=question, context=context, answer=answer
            ),
        },
    ]

    raw = chat_completion(messages, temperature=0.1)

    try:
        json_match = re.search(r"\{[\s\S]*\}", raw)
        if json_match:
            result = json.loads(json_match.group())
        else:
            result = json.loads(raw)
    except json.JSONDecodeError:
        logger.error("judge_parse_error", raw_response=raw[:200])
        result = {
            "relevance": {"score": 0, "reasoning": "Failed to parse judge response"},
            "correctness": {"score": 0, "reasoning": "Failed to parse judge response"},
            "completeness": {"score": 0, "reasoning": "Failed to parse judge response"},
            "faithfulness": {"score": 0, "reasoning": "Failed to parse judge response"},
            "overall_score": 0,
            "summary": "Evaluation failed due to parse error",
            "raw_response": raw[:500],
        }

    logger.info(
        "judge_evaluation_complete",
        overall_score=result.get("overall_score", "N/A"),
    )
    return result
